package day14_forLoop;

public class ForLoopPractice {

    public static void main(String[] args) {
        for (int i=10; i <= 5;i--){
            System.out.println("Hi COCO");

        }


        System.out.println("----------------------------------------------");
        //// 1 to 100

        for(int i = 1; i <= 100; i ++);

        System.out.println("------------------------");

       //// print A to Z

        for(char i = 'A';i <= 'Z'; i ++){
            System.out.println(i + "");



        }





    }

}
