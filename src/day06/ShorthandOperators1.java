package day06;

public class ShorthandOperators1 {
    public static void main(String[] args) {
         int x = 100;
        System.out.println(x);

        x = 200;

        System.out.println(x);

        int salary = 100000;

        salary += 10000;

        System.out.println(salary);

        int y = 2000;

        y -= 200;
        System.out.println(y);
    }
}
