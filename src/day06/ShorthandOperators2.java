package day06;

public class ShorthandOperators2 {
    public static void main(String[] args) {

        int number = 100;

        number *= 3;
        System.out.println(number);

        int expense = 10000;

        expense /= 2;
        System.out.println(expense);

        int amount = 100;
        amount %= 28;
        System.out.println(amount);
    }
}
