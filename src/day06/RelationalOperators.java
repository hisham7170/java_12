package day06;

public class RelationalOperators {
    public static void main(String[] args) {

        System.out.println(100 > 200);

        System.out.println(100 < 200);

        System.out.println(10 >= 10);

        System.out.println(100 <= 100);

        System.out.println(100 == 100);

        System.out.println('A'=='a');

        System.out.println(100 != 200);

        System.out.println(100 != 100);


        System.out.println(true != false);
    }
}
